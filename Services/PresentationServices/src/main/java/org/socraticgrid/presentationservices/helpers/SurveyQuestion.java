package org.socraticgrid.presentationservices.helpers;

import java.util.Map;

/**
 *
 * @author nhin
 */
public class SurveyQuestion {
    private String questionId;
    private String preLabel;
    private boolean required;
    private String answerType;
    private String suggestedControl;
    private boolean singleAnswer;
    private Map<String, String> possibleAnswers;

    public String getAnswerType() {
        return answerType;
    }

    public void setAnswerType(String answerType) {
        this.answerType = answerType;
    }

    public Map<String, String> getPossibleAnswers() {
        return possibleAnswers;
    }

    public void setPossibleAnswers(Map<String, String> possibleAnswers) {
        this.possibleAnswers = possibleAnswers;
    }

    public String getPreLabel() {
        return preLabel;
    }

    public void setPreLabel(String preLabel) {
        this.preLabel = preLabel;
    }

    public String getQuestionId() {
        return questionId;
    }

    public void setQuestionId(String questionId) {
        this.questionId = questionId;
    }

    public boolean isRequired() {
        return required;
    }

    public void setRequired(boolean required) {
        this.required = required;
    }

    public boolean isSingleAnswer() {
        return singleAnswer;
    }

    public void setSingleAnswer(boolean singleAnswer) {
        this.singleAnswer = singleAnswer;
    }

    public String getSuggestedControl() {
        return suggestedControl;
    }

    public void setSuggestedControl(String suggestedControl) {
        this.suggestedControl = suggestedControl;
    }
    
    
}
