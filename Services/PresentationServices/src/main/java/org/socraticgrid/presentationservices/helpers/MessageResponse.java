package org.socraticgrid.presentationservices.helpers;

import java.text.DateFormat;
import java.text.ParseException;
import java.text.SimpleDateFormat;
import java.util.ArrayList;
import java.util.Date;
import java.util.List;

/**
 *
 * @author jharby
 */
public class MessageResponse implements Comparable {

    private String messageId;
    private String type;
    private String location;
    private List<String> labels = new ArrayList<String>();
    private String messageDate;
    private String messageTime;
    private String description;
    private String from;
    private String title;
    private String status;
    private String priority;
    private int tasksCount;
    private int tasksComplete;

    @Override
    public int compareTo(Object t) {
        MessageResponse other = null;
        if (t != null && t instanceof MessageResponse) {
            other = (MessageResponse) t;
            if (other.getMessageDate() == null || other.getMessageTime() == null)
                return -10;
        }
        else {
            return -10;
        }
        DateFormat formatter;
        formatter = new SimpleDateFormat("yyyy-M-dd");
        Date d1 = null, d2 = null;
        try {
            d1 = (Date) formatter.parse(getMessageDate() + " " + getMessageTime());
            d2 = (Date) formatter.parse(other.getMessageDate() + " " + other.getMessageTime());
        }
        catch (ParseException e) {
            e.printStackTrace();
        }
        return d1.compareTo(d2);
    }

    public String getMessageType() {
        return type;
    }

    public void setMessageType(String messageType) {
        this.type = messageType;
    }

    public List<String> getLabels() {
        return labels;
    }

    public void setLabels(List<String> labels) {
        this.labels = labels;
    }

    public String getLocation() {
        return location;
    }

    public void setLocation(String location) {
        this.location = location;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getFrom() {
        return from;
    }

    public void setFrom(String from) {
        this.from = from;
    }

    public String getMessageDate() {
        return messageDate;
    }

    public void setMessageDate(String messageDate) {
        this.messageDate = messageDate;
    }

    public String getMessageId() {
        return messageId;
    }

    public void setMessageId(String messageId) {
        this.messageId = messageId;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String messageStatus) {
        this.status = messageStatus;
    }

    public String getMessageTime() {
        return messageTime;
    }

    public void setMessageTime(String messageTime) {
        this.messageTime = messageTime;
    }

    public String getPriority() {
        return priority;
    }

    public void setPriority(String priority) {
        this.priority = priority;
    }

    public int getTasksComplete() {
        return tasksComplete;
    }

    public void setTasksComplete(int tasksComplete) {
        this.tasksComplete = tasksComplete;
    }

    public int getTasksCount() {
        return tasksCount;
    }

    public void setTasksCount(int tasksCount) {
        this.tasksCount = tasksCount;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

}
